package com.shaio.his.Cirugia;

public class StructDescripcionQuirurgica {
	
	private String descripcion;
	private String complicaciones;
	private String descripcionComplicacion;
	private String patologia;
	private String estadoSalida;
	
	
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getComplicaciones() {
		return complicaciones;
	}
	public void setComplicaciones(String complicaciones) {
		this.complicaciones = complicaciones;
	}
	public String getDescripcionComplicacion() {
		return descripcionComplicacion;
	}
	public void setDescripcionComplicacion(String descripcionComplicacion) {
		this.descripcionComplicacion = descripcionComplicacion;
	}
	public String getPatologia() {
		return patologia;
	}
	public void setPatologia(String patologia) {
		this.patologia = patologia;
	}
	public String getEstadoSalida() {
		return estadoSalida;
	}
	public void setEstadoSalida(String estadoSalida) {
		this.estadoSalida = estadoSalida;
	}
	
}
